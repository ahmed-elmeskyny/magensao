import Head from "next/head";

//styles
import styles from "../styles/Home.module.scss";
import { useState, useEffect } from "react";

//components
import Layout from "../components/Layout/layout";
import Slider from "../components/slideshow/slider";
import Thumbnail from "../components/thumbnail/thumbnail";
import Abonner from "../components/abonner/abonner";
import Categorie from "../components/categorie/categorie";

//firebase
import fire, { getFormattedDate } from "../config/fire-config";
import { collection, onSnapshot } from "firebase/firestore";

export default function Home() {
  const [articles, setarticles] = useState([]);
  useEffect(() => {
    onSnapshot(collection(fire, "articles"), (snapshot) => {
      setarticles(snapshot.docs.map((doc) => ({ id: doc.id, ...doc.data() })));
    });
  }, []);
  console.log(articles);

  const sliderArticles = articles.filter(
    (article) => article.position == "slider"
  );
  sliderArticles.sort((a, b) => {
    return b.createdAt - a.createdAt;
  });

  const TopArticles = articles.filter((article) => article.position == "top");
  TopArticles.sort((a, b) => {
    return b.createdAt - a.createdAt;
  });

  const essentielArticles = articles.filter(
    (article) => article.position == "essentiel"
  );
  essentielArticles.sort((a, b) => {
    return b.createdAt - a.createdAt;
  });

  return (
    <div>
      <Head>
        <title>MagEnsao</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      {articles.length == 0 ? (
        <img src="/loader.svg" style={{ marginTop: "100px" }}></img>
      ) : (
        <Layout>
          <Slider articles={sliderArticles}></Slider>
          <div className={styles.topArticles}>
            <div className={styles.articles}>
              <h2 className={styles.title}>Articles du mois</h2>
              <div
                style={{
                  display: "flex",
                  flexWrap: "wrap",
                  // alignItems: "center",
                  justifyContent: "space-around",
                }}
              >
                {TopArticles.map((article) => (
                  <Thumbnail
                    img="/blog.png"
                    key={article.id}
                    {...article}
                  ></Thumbnail>
                ))}
              </div>
            </div>
            <div className={styles.newsContainer}>
              <h2 className={styles.essentiel}>L’ESSENTIEL</h2>
              {essentielArticles.map((article) => (
                <div className={styles.news} key={article.id}>
                  <p>
                    {article.categorie} -{" "}
                    <span>{getFormattedDate(article.createdAt)}</span>
                  </p>
                  <a href={`/article/${article.id}`}>{article.title}</a>
                </div>
              ))}
            </div>
          </div>

          <Abonner></Abonner>
          <Categorie categorie="Ingénierie"></Categorie>
          <Categorie categorie="Culture"></Categorie>
          <Categorie categorie="Ingénierie"></Categorie>
          <Categorie categorie="Culture"></Categorie>
        </Layout>
      )}
    </div>
  );
}
